/*
*********************************************************************************************************
*	函 数 名: DSO_FirFilter_Step1KHz
*	功能说明: FIR滤波器。
*	形    参: 无        	
*	返 回 值: 无
*********************************************************************************************************
*/
void DSO_FirFilter_Step1KHz(void)
{
	arm_fir_instance_f32 S;
	uint16_t i;
	
	/* 获取要滤波的数值 */
	for(i = 0; i < BLOCK_SIZE; i++)
	{
		FirDataInput[i] =g_usWaveBuf1[i+g_usCurTriPos+g_usCurTriStep];
	}
	
	/* FIR低通滤波器，截止频率100KHz */
	if(g_ucFirFlter_Step1KHz == 1)
	{
		arm_fir_init_f32(&S, NUM_TAPS, (float32_t *)&firCoeffs32LP_1KHz[0], &firStateF32[0], BLOCK_SIZE);
	}
	/* FIR低通滤波器，截止频率200KHz */
	else if(g_ucFirFlter_Step1KHz == 2)
	{
		arm_fir_init_f32(&S, NUM_TAPS, (float32_t *)&firCoeffs32LP_2KHz[0], &firStateF32[0], BLOCK_SIZE);
	}
	/* FIR低通滤波器，截止频率300KHz */
	else if(g_ucFirFlter_Step1KHz == 3)
	{
		arm_fir_init_f32(&S, NUM_TAPS, (float32_t *)&firCoeffs32LP_3KHz[0], &firStateF32[0], BLOCK_SIZE);
	}
	/* FIR低通滤波器，截止频率400KHz */
	else if(g_ucFirFlter_Step1KHz == 4)
	{
		arm_fir_init_f32(&S, NUM_TAPS, (float32_t *)&firCoeffs32LP_4KHz[0], &firStateF32[0], BLOCK_SIZE);
	}
	/* FIR低通滤波器，截止频率500KHz */
	else if(g_ucFirFlter_Step1KHz == 5)
	{
		arm_fir_init_f32(&S, NUM_TAPS, (float32_t *)&firCoeffs32LP_5KHz[0], &firStateF32[0], BLOCK_SIZE);
	}
	/* FIR低通滤波器，截止频率600KHz */
	else if(g_ucFirFlter_Step1KHz == 6)
	{
		arm_fir_init_f32(&S, NUM_TAPS, (float32_t *)&firCoeffs32LP_6KHz[0], &firStateF32[0], BLOCK_SIZE);
	}
	/* FIR低通滤波器，截止频率700KHz */
	else if(g_ucFirFlter_Step1KHz == 7)
	{
		arm_fir_init_f32(&S, NUM_TAPS, (float32_t *)&firCoeffs32LP_7KHz[0], &firStateF32[0], BLOCK_SIZE);
	}
	/* FIR低通滤波器，截止频率800KHz */
	else if(g_ucFirFlter_Step1KHz == 8)
	{
		arm_fir_init_f32(&S, NUM_TAPS, (float32_t *)&firCoeffs32LP_8KHz[0], &firStateF32[0], BLOCK_SIZE);
	}
	/* FIR低通滤波器，截止频率900KHz */
	else if(g_ucFirFlter_Step1KHz == 9)
	{
		arm_fir_init_f32(&S, NUM_TAPS, (float32_t *)&firCoeffs32LP_9KHz[0], &firStateF32[0], BLOCK_SIZE);
	}
	/* FIR低通滤波器，截止频率1000KHz */
	else if(g_ucFirFlter_Step1KHz == 10)
	{
		arm_fir_init_f32(&S, NUM_TAPS, (float32_t *)&firCoeffs32LP_10KKKHz[0], &firStateF32[0], BLOCK_SIZE);
	}
	
	/* 80阶FIR滤波 */
	arm_fir_f32(&S, FirDataInput, FirDataOutput, BLOCK_SIZE);
	
	/* 输出滤波器数值 */
	for(i = 0; i < DSOSCREEN_LENGTH; i++)
	{
		g_usWaveBuf[i] =FirDataOutput[i+50];
	}
}

